// Real Dom vs Virtual Dom
/*
  Real Dom: The Real DOM (Document Object Model) represents the structure of an HTML document in the form of a tree, where each node corresponds to an element in the document. It is an interface that allows programming languages, such as JavaScript, to manipulate and interact with the content, structure, and style of a webpage.
  
  Virtual Dom: The Virtual DOM is a concept used by modern frameworks like React to optimize the rendering process. It is a lightweight, in-memory representation of the Real DOM that allows developers to make changes without directly manipulating the on-screen elements.  
*/

