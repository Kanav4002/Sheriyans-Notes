/*
  React Hooks, enable functional components to use state, lifecycle, and other React features without relying on class components.

  Eliminate the need for class components for state and side-effect management.
  Improve code readability and encourage a functional programming style.
  Widely adopted in modern React projects for cleaner and more maintainable code.

  Types of React Hooks:

  1. useState - state ko manage karne ke liye.
  2. useEffect - side effects handle karne ke liye
  (jaise API call, DOM manipulation, event
  listener).
  3. useContext - global state ko consume karne ke
  liye without prop drilling.
  4. useReducer - complex state management ke liye
  (Redux jaisa chhota version).
  5. useRef - mutable values hold karne ke liye jo re-
  render trigger na karein, ya DOM access karne ke
  liye.
  6. useMemo & useCallback - optimization ke liye,
  unnecessary re-renders avoid karne ke liye.
*/
